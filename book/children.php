			“아이는 영어로 차일드지만, 복수형은 차일즈가 아니라 칠드런이잖아. 완전히 다른 존재가 되는 거라고.”<br>
			애들은 그런 거다, 진나이 씨가 자주 하는 말이었다.<br>
			_「칠드런」 107쪽</p>
			<p>“우리 일이 그거라고.”<br>
			“그게 뭔데?”<br>
			“우리는 기적을 일으키는 사람이야.”<br>
