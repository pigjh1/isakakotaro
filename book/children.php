		<div class="col">
			이사카 월드 초심자에게 추천하는 기념비적인 첫 작품집</p>
			<p>소설가 중에도 올라운더는 존재한다. 장편과 단편 모두를 능숙하게 넘나들며, 나아가 다양한 장르의 이야기를 자유자재로 쓸 수 있는 작가. 이사카 코타로는 뛰어난 올라운더다. 그러한 면이 단적으로 나타난 것이 『칠드런』이며, 각 단편의 완성도는 물론이거니와 전체적인 구성에서도 독자적인 시도가 돋보인다.<br>
			_ 가야마 후미로(평론가)</p>
			<p>이사카 코타로의 대표작의 하나이자 2005년 국내에 그의 이름을 처음 알렸던 『칠드런』이 현대문학에서 최고은의 번역으로 새롭게 출간되었다. <소설 겐다이> 2002년 4월호에서 2004년 3월호에 걸쳐 발표된 다섯 편의 연작소설을 묶은 『칠드런』은 문장과 대화의 통통 튀는 재미, 조형력이 돋보이는 개성적인 등장인물과 기상천외한 설정, 산뜻한 감동 등 ‘이사카 코타로적’인 것들이 고루 담긴 그의 기념비적인 첫 작품집이다. 그뿐만 아니라 그가 즐겨 쓰는, 한 작품의 인물이 다른 작품에 살짝 등장하는 식으로 작품 간에 미묘한 연결 고리를 두는 세계관의 공유를 중간중간 발견할 수 있어 이사카 코타로를 처음 접하는 독자라면 가장 먼저 읽어 볼 만한 책이다. 특히 이번의 『칠드런』은 일본에서 2007년 발행된 문고본을 번역한 것으로 평론가 가야마 후미로의 작품 해설이 더해져 그즈음 한창 조명되던 신예 작가 이사카 코타로를 다시금 만날 수 있게 한다.<br>
			이사카 코타로는 『칠드런』에 대해 “단편집인 척하는 장편소설”이라 평했는데, 「뱅크」「칠드런」「레트리버」「칠드런 2」「인」으로 이어지는 다섯 개의 단편은 저마다 배경이 다르고 화자 역시 다르지만 읽어 나가다 보면 결국 한 사람의 이야기가 밑바탕에 그려진다. 그리고 그 한 사람이 바로 이사카 코타로의 등장인물 중에서 여전히 많은 독자들이 기억하고 사랑하는 남자 진나이이다. 그는 터무니없는 말로 상대를 얼떨떨하게 만드는 괴짜에다 천상천하 유아독존, 방약무인, 마이동풍 등 자기중심적인 사람을 가리키는 사자성어가 무척 잘 어울리며 자신만의 정의를 가지고 주변을 자기 페이스로 끌어들이지만 어쩐지 미워할 수는 없는 인물이다. 게다가 불가사의하게도 그는 항상 소동의 중심에 있으니 사건이 그를 따라다닌다고 할 법하다. 흥미로운 것은 『칠드런』이 진나이의 이야기를 그리고 있지만 그와 가장 가까이 지내는 네 사람―가모이, 무토, 유코, 나가세의 시선으로 펼쳐진다는 점이다. 아울러 다섯 개의 단편이 시간순이 아니라 과거와 현재를 오가며 전개되기 때문에 이야기의 궤적을 좇다 보면 어느 순간 기묘한 카타르시스를 경험하게 된다.</p>
			<p>불가사의한 훈훈함을 선사하는 다섯 개의 기적 이야기</p>
			<p>“우리는 기적을 일으키는 사람이야.”<br>
			순간 주변이 쥐 죽은 듯 조용해졌다.<br>
			“소년의 건전한 육성? 평화로운 가정생활? 소년법과 가정심판법의 목적? 그딴 거 전부 거짓이야. 아무래도 상관없다고. 우리 목적은 기적을 일으키는 것. 그뿐이야.”<br>
			옆에 있던 우리는 곤혹스러워했지만, 진나이 씨는 아랑곳하지 않고 더욱 소리 높여 외쳤다.<br>
			“안되는 놈들은 안된다고 했지? 절대로 새사람이 될 수 없다고. 지구의 자전이 멈춘다 해도, 지구온난화가 기적처럼 멈춰도, 암 특효약이 발명되어도, 스티븐 시걸이 악역한테 져도, 비행 청소년이 새사람이 되는 일은 없다고 단언했지?”<br>
			“그렇게까지는 말 안 했잖아.”<br>
			중년 남자가 화를 냈다. 나 역시 같은 생각이었지만 진나이 씨는 아랑곳하지 않았다.<br>
			“그걸 우리가 한다고.” 진나이 씨는 흡족한 얼굴로 웃었다. “우리는 기적을 일으키는 사람들이야. 그런데 당신들은 기적을 일으킬 수 있나?”<br>
			_「칠드런 2」 212~213쪽</p>
			<p>왜 은행 강도는 인질 모두에게 야시장에서나 팔 법한 만화영화 인물 가면을 씌웠을까? 첫 번째 단편 「뱅크」에서 센다이의 대학생 가모이는 친구 진나이에게 이끌려 영업 종료 직전의 은행에 갔다가 강도와 맞닥뜨려 인질이 된다. 어떤 일에든 맞서 싸우는 게 신조인 진나이는 비틀스의 노래를 부르는 등 저항을 시도하지만 상황은 악화되기만 하고, 그런 진나이의 행동에 속으로 투덜대던 가모이는 함께 인질로 붙잡힌 시각장애인 청년 나가세로부터 사건에 관한 생각지도 못한 이야기를 듣게 된다.<br>
			아버지와 아들은 정말로 어머니를 살해해 정원에 묻었을까? 두 번째 단편이며 표제작인 「칠드런」에서는 순진하고 성실한 가정법원 조사관 무토가 절도를 저지른 고등학생과 그의 무뚝뚝한 아버지를 면접조사 하면서 어딘가 수상쩍은 구석이 있는 부자를 추적하게 되는 이야기이다. 「뱅크」로부터 12년 후의 이야기로, 놀랍게도 진나이는 무토의 선배이자 가정법원 조사관으로 일하고 있다. 큰 줄기는 비행소년과 가사 분쟁을 해결하는 가정법원 조사관이 하는 일을 다루고 있지만 30대가 되어서도 여전한 진나이의 엉뚱한 조언들로 사건은 예상하지 못한 방향으로 흘러간다.<br>
			계속 머물러 있는 사람과 반복되는 행동들…… 시간이 멈춘 듯한 벤치 주변의 비밀은 무엇일까? 세 번째 단편 「레트리버」는 「칠드런」에서 다시 과거로 돌아가 「뱅크」에서 만났던 나가세의 연인, 유코의 회상으로 진행된다. 나가세와 유코는 센다이 역 근처에서 실연한 진나이를 위로하는데, 진나이가 뜬금없이 트루먼 커포티 소설의 한 구절을 인용하면서 주변의 시간이 멈추었다는 말을 꺼낸다. 과연 그들 주변의 벤치에는 두 시간 전부터 같은 사람들이 계속 앉아 같은 행동을 하고 있었다. 진나이와 친구들은 초자연적이라고도 할 만한 이 불가사의를 파헤치기 위해 움직이기 시작한다.<br>
			네 번째 단편 「칠드런 2」는 「칠드런」에서 1년 후의 이야기로, 진나이가 시험 관찰 중인 소년과 무토가 담당한 이혼 조정 중의 부부가 등장한다. 사건다운 사건은 일어나지 않지만 진나이의 기이한 행동이 역시 의외의 결과를 불러일으키며, 「칠드런」에서 얼핏 드러났던 가정법원 조사관의 진면목이 이 작품에서 유감없이 펼쳐진다.<br>
			이 사람은 정말로 진나이가 맞을까? 마지막 단편 「인」은 다시 과거로 돌아가 「뱅크」에서부터 1년 후의 이야기이다. 나가세와 유코는 진나이가 아르바이트한다는 백화점 옥상에 찾아가지만 진나이는 보이지 않고, 어느덧 혼자 남겨진 나가세는 소소한 모험을 겪다가 진나이를 만나지만 어쩐지 그의 모든 것이 평소와는 다름을 느끼게 된다.<br>
			『칠드런』의 등장인물들에게 있어 진나이는 자신들의 일상에 던져진 비일상적인 존재이다. 그들은 진나이를 통해 작은 위안을 얻고, 세계를 다시 바라보는 인식의 변화를 겪는다. 독자들 역시 『칠드런』을 읽음으로써 지금 서 있는 현실을 되돌아보고 어떻게 살아가야 할 것인지를 생각해 볼 수 있을 것이다.</p>
