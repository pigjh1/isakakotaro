		<h3>책속의한줄</h3>
		<div class="col">
			<p>“아이들과 마주하는 데 심리학이며 사회학이 다 뭡니까. 녀석들은 통계가 아니고, 수학도, 화학식도 아니라고요. 맞죠? 그리고 누구나 자기는 온리 원이라고 생각해요. 누군가와 닮았다는 소리는 질색한다고요. 누가 나한테 존 레넌하고 닮았다고 해도 싫습니다. 그런데 조사관이 ‘아, 얘는 이런 가정환경 패턴이군’ ‘이건 예전에 다뤘던 사건하고 같은 경우네’ 하고 틀에 끼워 맞추면 기분이 좋겠습니까? 아니에요? 밸런타인데이에 다른 남자애들하고 같은 초콜릿을 받는 거나 마찬가지라고요. 좋아하는 애한테 초콜릿을 받고 좋아서 풀어 봤는데 다른 애들한테 뿌린 싸구려 초콜릿이었다는 것만큼 비극적이라고요. 비극은 필요 없어요. 조사관은 담당하는 아이가 ‘다른 누구와도 다른, 세상에서 하나뿐인 아이’라고 생각하고 마주하지 않으면 안 된다고요.”<br>
			_「칠드런」 85쪽</p>
			<p>“세상에 좋은 부모는 없지만, 부모의 영향을 받지 않는 아이도 없지 않을까?”<br>
			“하지만 비행의 원인이 꼭 부모 탓인 것 같지는 않아요.” 시로는 말을 이었다. “내 주변에는 심심풀이로 범죄를 저지르는 애들도 있거든요. 그런 애들이 더 많지 않을까요?” 시로는 감자튀김을 집어 와그작와그작 씹었다. “아저씨 같은 조사관들을 감쪽같이 속이는 약아빠진 애들이요.”<br>
