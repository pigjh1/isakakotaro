			일본 최고 권위의 나오키상 후보에 다섯 번이나 오르고 일본 작가로서는 유일하게 서점대상에 5회 연속 노미네이트된, 현재 일본을 대표하는 젊은 작가 이사카 코타로의 신작 장편소설 『오! 파더』가 북홀릭에서 출간된다.<br>
			이사카 코타로의 작품은 복잡한 사회문제를 다루기 시작한 『골든 슬럼버』를 경계로 제1기와 제2기로 구분되는데, 신문 연재소설 『오! 파더』는 재기발랄함이 특징인 제1기 마지막 장편소설이다.</p>

			<p>비틀즈를 연상시키는 네 명의 아버지, 그리고 엄친아 아들<br>
			고등학교 2학년 남학생 유키오는 화려한 연애 경력의 어머니 덕에 무려 네 명의 아버지들과 한 집에서 살고 있다. 그런데 흡사 비틀즈를 연상시키는 이 네 명의 아버지들의 이력이 심상치가 않다. 도박을 즐기며 자신의 직감을 신봉하며 살아가는 타카, 전직 호스트로 여자들의 시선을 한 몸에 받는 꽃중년 아오이, 항상 책을 끼고 사는 대학교수 사토루, 그리고 격투기 마니아인 몸짱 중학교 교사 이사오. 아들 유키오는 언뜻 평범해 보이지만, 각기 개성 다른 네 아버지 밑에서 자란 덕분에 친구들 눈에는 그야말로 완벽한 친구. 이런 엄친아 주인공이기에 사건이 터지면 은근히 그를 찾는 친구들이 많다. 그러다 이번에는 중학교 동창을 오랜만에 만나면서 유키오는 어마어마한 사건에 휘말리고 마는데…….<br>
			오, 파더! 이 아들을 굽어살펴 주옵소서.</p>

