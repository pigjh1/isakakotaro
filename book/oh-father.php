			매번 기발한 발상의 작품을 들고 독자를 찾아오는 작가 이사카 코타로는 이번 작품에서 네 명의 아버지와 한 지붕 아래 사는 지금의 상황을 친구들에게 비밀에 부쳐야 하는 남고생 주인공을 그렸다. 아들의 일거수일투족에 지대한 관심을 보이는 네 아버지와 그런 아버지들에게 무심하고 까칠하기만 한 아들이 서로 티격태격 주고받는 대화는 시종 독자들에게 웃음을 선사하며 쉼 없이 책장을 넘기게 한다. 또한 작가는 개성 만점의 캐릭터들을 속속 등장시켜 새로운 사건을 만들어 가며 작품의 긴장감을 한껏 고조시킨다.<br>
			그렇게 팽팽한 긴장감 속에 진행되던 이야기는 작품 후반에 가서는 다시 한 번 큰 전환을 맞게 된다. 그간 이름으로 불려 왔던 아버지들이 위기에 봉착한 아들에게 ‘아버지’라는 호칭으로 불린 순간, 아버지들은 모성애보다 강한 부성애를 발휘, 팀워크를 자랑하며 아들 구출 작전에 나선다. 서로 데면데면 보이던 부자지간이 돈독한 가족애를 드러내 보이며 독자의 눈물샘을 자극하는 장면이다.<br>
			페이지 후미에 이르러서는 작품 여기저기에 흩뿌려 두었던 복선들을 말끔히 회수시키며 작가만의 역량을 한껏 발휘, 이사카 월드 제1기 마지막 작품에 화룡점정을 찍는다.</p>

