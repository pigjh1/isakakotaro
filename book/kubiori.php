			<p><strong>연쇄살인범 ‘목 부러뜨리는 남자’는 친절을 베풀고<br>
			아버지는 죽은 아들을 위해 ‘복수자’가 되고<br>
			노부인은 50년 전의 ‘첫사랑’에 빠져들고<br>
			작가는 ‘사슴벌레’를 관찰하며 소설을 쓰고<br>
			도둑은 대중매체의 ‘악의’에 습격당하고<br>
			역사 속 ‘괴담’에 현재의 목격자는 전율하고<br>
