			<p>목을 부러뜨려 사람을 죽이는 연쇄살인 사건 용의자의 인상착의가 이웃 아파트에 사는 청년과 비슷하다는 노부부의 대화에서 시작되는 「목 부러뜨리는 남자의 주변」은 이 ‘의심하는 부부’, 목 부러뜨리는 남자와 꼭 닮은 강건한 외모의 심약한 청년 ‘오인당한 남자’, 장난으로 던진 말 때문에 왕따를 당하게 된 중학생 ‘괴롭힘 당하는 소년’의 이야기가 번갈아 펼쳐지면서 진짜 목 부러뜨리는 남자와의 기묘한 인연으로 휘말려 들어간다.<br>
			‘잔인무도한 살인귀라면 얼마나 편할까요?’ 교통사고로 아들을 잃은 남자의 독백으로 시작되는 「누명 이야기」는 ‘야마모토슈고로상 작가 특집’을 위한 단편으로, 독백이라는 형식에 구조적인 재미를 더해 아들을 차로 친 여자를 향한 복수가 완전범죄의 ‘이야기’로 완성되어 가는 과정이 시공간을 넘나들며 펼쳐진다.<br>
			‘수병 리베 나의 배’라는 일본의 원소 주기율표 암기 노래로 시작되는 「나의 배」는 이사카 고타로가 ‘마지막 사랑’이란 주제로 시도한 연애물로, 여기서 노부인은 50년 전 단 나흘 만났던 첫사랑을 찾아 달라고 탐정에게 의뢰한다. H(수소), He(헬륨), Li(리튬), Be(베릴륨), B(붕소), C(탄소), N(질소), O(산소), F(불소), Ne(네온)로 이어지는 원소기호에 얽힌 옛사랑의 추억이 의외의 결말을 이끌어 낸다.<br>
			‘세상엔 하느님도, 부처님도 없어’로 시작되어 이 문장이 반복적으로 등장하는 「사람답게」는 순문학 잡지에 쓴 단편으로, 외도를 저지른 남자를 조사하는 탐정과 사슴벌레를 관찰하는 작가의 이야기, 이유 없는 악의에 괴롭힘 당하는 소년의 이야기가 번갈아 펼쳐지면서 흥미진진한 전개를 보인다.<br>
			원래 장편소설용 기획으로 아껴 두었다가 단편으로 전환했다는, ‘월요일’에서 ‘일요일’까지 요일별로 흘러가는 구성의 「월요일에서 벗어나」는 도둑질하는 장면이 찍힌 영상으로 방송국 관계자에게 협박당하는 탐정의 이야기가 그려진다.<br>
			이사카 고타로가 처음으로 쓴 괴담인 「측근 이야기」는 잘나가는 친구의 자랑을 들으며 소소한 복수를 꾀하려던 작가가 우연찮게 400년 전 역사 속 인물의 저주가 현재에 되살아나는 모습과 맞닥뜨리게 되는 단편이다.<br>
			레몽 크노의 『문체 연습』에 자극을 받아 쓴 단편이라는 「미팅 이야기」는 줄거리에 점차로 살을 붙여 나가는 글쓰기 과정이 고스란히 드러나 있으며 광고문 형식, 대사 형식, 문자 메시지 형식 등 『목 부러뜨리는 남자를 위한 협주곡』을 통틀어 가장 자유분방한 구성을 보이는 이야기이다.<br>
			책의 제목처럼 마치 ‘협주곡’과 같은 울림을 빚어내는 이 일곱 편의 이야기는 구성이나 성격이 전혀 다른데, 전체를 아우르는 메시지는 바로 ‘세상의 균형’이다. 살인과 집단 괴롭힘, 복수, 악의 같은 어둡고 무거운 소재가 전면에서 다루어지고 있고 각각의 단편 속 등장인물들의 인생에는 다양한 고난과 고통이 존재한다. 그럼에도 행복하고 즐거운 일들 또한 마찬가지로 균형적으로 존재한다는 이사카 고타로의 긍정적인 시선이 옮긴이의 표현처럼 ‘판도라의 상자 속 희망’으로 『목 부러뜨리는 남자를 위한 협주곡』의 바탕에 흐르고 있다.<br>
			아울러 매 단행본마다 치밀한 구성력을 자랑하는 이사카 고타로답게 이 책에도 구석구석 숨겨진 단서들이 있다. 알면 알수록 그가 쳐 놓은 그물은 더욱더 촘촘하게 독자에게 다가온다. 한편, 『목 부러뜨리는 남자를 위한 협주곡』에는 이사카 고타로의 열혈 독자라면 반가워할 인물이 등장하는데 그 스스로도 좋아한다고 밝힌 ‘도둑’ 겸 ‘탐정’ 구로사와이다. 이 외에도 이전 작품들에서 등장한 인물들이 슬그머니 얼굴을 내비치는데 이러한 연결 고리를 찾는 일은 독자의 또 다른 즐거움이 될 것이다.</p>
